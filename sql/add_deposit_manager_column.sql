-- 담당자 컬럼 추가 (status 컬럼 뒤)
ALTER TABLE `deposit` 
ADD COLUMN `manager` VARCHAR(100) NULL COMMENT '담당자' 
AFTER `status`;

-- 인덱스 추가 (성능 최적화)
CREATE INDEX `idx_manager` ON `deposit` (`manager`);

