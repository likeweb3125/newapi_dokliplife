-- =============================================
-- parkStatus 테이블에 cost 컬럼 추가 SQL
-- =============================================

-- cost 컬럼 추가 (주차비)
-- 주의: 이미 컬럼이 존재하는 경우 오류가 발생할 수 있습니다.
ALTER TABLE `parkStatus` 
ADD COLUMN `cost` INT(11) NULL DEFAULT 0 COMMENT '주차비' 
AFTER `parkNumber`;

-- cost 인덱스 추가 (성능 최적화)
-- 주의: 이미 인덱스가 존재하는 경우 오류가 발생할 수 있습니다.
CREATE INDEX `idx_parkStatus_cost` ON `parkStatus` (`cost`);

